package bloque3;

/**
 * Desarrolla un programa que declare una variable de tipo double y otra de tipo float, y luego imprima su diferencia.
 */
public class Ejercicio12 {
    public static void main(String[] args) {
        double numero1 = 1.878;
        float numero2 = 1.98732f;
        double diferencia = numero1 - numero2;

        System.out.print("La diferencia es " + diferencia);
    }
}
