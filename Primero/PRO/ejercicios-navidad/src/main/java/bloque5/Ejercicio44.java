package bloque5;


/**
 * Escribe un programa que determine si una variable booleana ingresada por el usuario es verdadera o falsa y muestre
 * un mensaje correspondiente.
 */
public class Ejercicio44 {
    public static void main(String[] args) {
        boolean esPar = false;
        if (esPar) {
            System.out.println("La variable es falsa");
        }else {
            System.out.println("La variable es verdadera");
        }
    }
}
