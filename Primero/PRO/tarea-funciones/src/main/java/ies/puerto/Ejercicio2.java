package ies.puerto;

/**
 * Clase que tiene como objetivo calcular el area de un circulo.
 * @author Melissa
 */
public class Ejercicio2 {
    /**
     * Funcion que calcula el area de un circulo
     * @param radio establece el radio para realizar el cálculo
     * @return devuelve el resultado (radio²*pi)
     */
    public double areaCirculo(int radio) {
        double resultado;
        double pi = 3.14159265359;

        return resultado = pi*(radio * radio);
    }
}