package bloque3;

/**
 * Desarrolla un programa que tome dos cadenas ingresadas por el usuario y muestre la concatenación de ambas, así como su longitud.
 */
public class Ejercicio19 {
    public static void main(String[] args) {
        String cadena1 = "Hola";
        String cadena2 = "Mundo";
        System.out.println("Cadenas concatenadas: " + cadena1 + " " + cadena2 + "\nLongitud de primera cadena: " + cadena1.length() + "\nLongitud de segunda cadena: " + cadena2.length());
    }
}
