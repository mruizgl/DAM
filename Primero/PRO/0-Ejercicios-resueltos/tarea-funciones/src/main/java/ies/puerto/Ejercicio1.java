package ies.puerto;

/**
 * Clase que tiene como obejtivo calcular el área de un triangulo
 * @author Melissa
 */

public class Ejercicio1 {
    /**
     * Funcion que calcula el área de un triangulo.
     * @param base La base que definimos del triangulo
     * @param altura Altura del triangulo
     * @return devuelve el resultado calculando el area del triangulo (b*a)/2
     */
    public int areaTriangulo(int base, int altura) {

        return (base*altura)/2;
    }

}